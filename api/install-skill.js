// Vercel Serverless Function вЂ” generates a pre-filled installer for the
// /classify-inspiration skill on demand, reading secrets from Vercel env vars
// so nothing sensitive ever lives in git.
//
// Usage (what teammates paste in their Terminal):
//   Mac:     curl -fsSL 'https://immuvi-command-center.vercel.app/api/install-skill?t=<secret>' | bash
//   Win+WSL: curl -fsSL 'https://immuvi-command-center.vercel.app/api/install-skill?t=<secret>&os=wsl' | bash
//
// The `t` query param must match INSTALL_SKILL_SECRET (rotatable env var).
// If it doesn't, we return 401 and DO NOT include any credentials in the
// response body. Even the error message is safe to paste publicly.
//
// Env vars this function reads (set via `vercel env add`):
//   - INSTALL_SKILL_SECRET       : the shared "team secret"; rotate if leaked
//   - SUPABASE_SERVICE_ROLE_KEY  : piped into the teammate's ~/.classify-inspiration.env
//   - SUPABASE_DB_PASSWORD       : same
//
// OS branching:
//   - mac  (default): uses Homebrew for ffmpeg/yt-dlp, expects macOS/Darwin.
//   - wsl           : uses apt-get (sudo prompt), expects Linux/WSL. This is
//                     the supported path for Windows teammates because the
//                     skill itself is bash-only (psql, heredocs, $HOME, вЂ¦)
//                     and would require a full PowerShell rewrite to run
//                     natively on Windows вЂ” not worth it for ~5min of WSL.

export default async function handler(req, res) {
  // No caching вЂ” if creds rotate, the next curl must see fresh values.
  res.setHeader('Cache-Control', 'no-store, max-age=0');

  const provided = (req.query && req.query.t) || '';
  const expected = process.env.INSTALL_SKILL_SECRET || '';
  if (!expected) {
    res.setHeader('Content-Type', 'text/plain; charset=utf-8');
    res.status(500).send('Installer disabled: INSTALL_SKILL_SECRET not configured.\n');
    return;
  }
  if (provided.length !== expected.length || !timingSafeEqual(provided, expected)) {
    res.setHeader('Content-Type', 'text/plain; charset=utf-8');
    res.status(401).send('Unauthorized. Contact Gaurav for the correct install link.\n');
    return;
  }

  const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
  const dbPassword = process.env.SUPABASE_DB_PASSWORD;
  if (!serviceKey || !dbPassword) {
    res.setHeader('Content-Type', 'text/plain; charset=utf-8');
    res.status(500).send('Installer misconfigured: SUPABASE_* env vars missing on the server.\n');
    return;
  }

  // Normalize os param. Accept mac/macos/darwin or wsl/linux/ubuntu. Default: mac.
  const osRaw = String((req.query && req.query.os) || 'mac').toLowerCase();
  const os = /^(wsl|linux|ubuntu|debian)/.test(osRaw) ? 'wsl' : 'mac';

  // bash-safe single-quote escape (wraps value in '...' and escapes any ')
  const sq = (s) => "'" + String(s).replace(/'/g, "'\\''") + "'";
  const SRK = sq(serviceKey);
  const DBP = sq(dbPassword);

  const version = `1.2.0-prefilled-${os}`;
  const assetBase = `https://immuvi-command-center.vercel.app/team-skill`;
  const generatedAt = new Date().toISOString();

  const script = os === 'wsl'
    ? buildWslInstaller({ SRK, DBP, version, assetBase, generatedAt })
    : buildMacInstaller({ SRK, DBP, version, assetBase, generatedAt });

  res.setHeader('Content-Type', 'text/x-shellscript; charset=utf-8');
  res.status(200).send(script);
}

